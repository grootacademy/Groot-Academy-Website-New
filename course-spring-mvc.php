                        <h5 class="text-gradient text-primary mb-0 mt-2">1.What is Spring MVC? </h5>
                        <p>Answer: Spring MVC, or Model-View-Controller, is a framework within the Spring Framework that facilitates the development of web applications in Java. It follows the MVC design pattern, separating an application into model (business logic), view (user interface), and controller (request handling).</p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">2. Why should I learn Spring MVC?</h5>
                        <p>Answer: Learning Spring MVC offers several advantages, including simplified development of web applications, improved testability through dependency injection, and seamless integration with other Spring technologies. It is widely used in the industry and enhances your employability as a Java developer.</p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">3.What are the key components of Spring MVC?</h5>
                        <p>Answer: The key components include:<br>
                            Model: Represents the application's data and business logic.<br>
                            View: Displays the data to the user and handles user input.<br>
                            Controller: Manages the flow of the application, handling user requests, and interacting with the model and view.</p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">4. Is Spring MVC suitable for large-scale applications?</h5>
                        <p>Answer: Yes, Spring MVC is well-suited for large-scale applications. Its modular and flexible architecture allows developers to scale applications effectively. It also integrates seamlessly with other Spring projects, making it a popular choice for enterprise-level development.</p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">5.What is the role of the DispatcherServlet?</h5>
                        <p>Answer: The DispatcherServlet is the front controller in a Spring MVC application. It receives all incoming requests and manages the flow of the request processing. It dispatches requests to the appropriate controllers, views, and models.</p>
