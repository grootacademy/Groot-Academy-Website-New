                        <h5 class="text-gradient text-primary mb-0 mt-2">6.How does Dependency Injection (DI) work in Spring MVC? </h5>
                        <p>Answer: Dependency Injection in Spring MVC involves injecting objects (dependencies) into a class rather than the class creating its dependencies. This is achieved through inversion of control (IoC) using annotations or XML configuration, making code modular, testable, and easier to maintain.</p>
