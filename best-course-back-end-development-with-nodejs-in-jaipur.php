                    <div class="col-lg-6">
                        <h5 class="text-gradient text-primary mb-0 mt-2">1. What is Node.js?</h5>
                        <p>Answer:Node.js is a server-side JavaScript runtime environment that allows developers to build scalable and high-performance network applications. </p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">2. How does Node.js differ from traditional backend technologies?</h5>
                        <p>Answer:Node.js is non-blocking and event-driven, making it suitable for handling concurrent requests efficiently. Traditional backend technologies often use a multi-threaded approach. </p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">3.What is npm?</h5>
                        <p>Answer: npm (Node Package Manager) is the package manager for Node.js. It allows developers to easily manage and install third-party packages and libraries.</p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">4. What is Express.js?</h5>
                        <p>Answer: Express.js is a popular web application framework for Node.js. It simplifies the process of building robust and scalable web applications by providing a set of features for routing, middleware, and more.</p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">5.How do I handle asynchronous operations in Node.js?</h5>
                        <p>Answer: Node.js uses callbacks, Promises, and async/await to handle asynchronous operations. Callbacks are common, but Promises and async/await provide a more readable and maintainable code structure.</p>
                    </div>
                    <div class="col-lg-6">
                        <h5 class="text-gradient text-primary mb-0 mt-2">6. What is middleware in Express.js?</h5>
                        <p>Answer:Middleware in Express.js are functions that have access to the request, response, and the next middleware function in the application’s request-response cycle. They can be used to perform tasks like logging, authentication, etc. </p>
                        <h5 class="text-gradient text-primary mb-0 mt-2">7.How do I connect to a database in Node.js? </h5>
                        <p>Answer: There are several database options for Node.js, including MongoDB, MySQL, PostgreSQL, etc. You can use libraries like Mongoose for MongoDB or Sequelize for SQL databases to interact with databases.</p>
