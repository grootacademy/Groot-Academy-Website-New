            <h5 class="text-gradient text-primary mb-0 mt-2">1:What is data science?</h5>
            <p>Ans: Data science is a multidisciplinary field that uses scientific methods, processes, algorithms, and systems to extract insights and knowledge from structured and unstructured data.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">2:What are the key skills required for a career in data science?</h5>
            <p>Ans: Key skills include programming (e.g., Python, R), statistical analysis, machine learning, data cleaning and preprocessing, data visualization, and domain-specific knowledge.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">3:How does data science differ from traditional statistics?</h5>
            <p>Ans: While both involve analyzing data, data science often deals with larger datasets, incorporates machine learning for predictive modeling, and focuses on extracting actionable insights from data.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">4: What is the CRISP-DM methodology in data science?</h5>
            <p>Ans: CRISP-DM (Cross-Industry Standard Process for Data Mining) is a widely used methodology in data science that outlines the steps involved in a data mining or analytics project, including business understanding, data preparation, modeling, evaluation, and deployment.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">5:Explain the concept of machine learning in data science.</h5>
            <p>Ans:Machine learning is a subset of artificial intelligence that involves the development of algorithms and models that enable systems to learn and make predictions or decisions without being explicitly programmed.</p>

        </div>
        <div class="col-lg-6">
            <h5 class="text-gradient text-primary mb-0 mt-2">6: What is the significance of feature engineering in machine learning?</h5>
            <p>Ans: Feature engineering involves selecting and transforming variables to improve model performance. It is a critical step in building effective machine learning models.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">7:How is data science used in business?</h5>
            <p>Ans:Data science helps businesses make data-driven decisions, optimize processes, improve customer experiences, and gain a competitive edge through insights derived from data analysis.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">8:What is the role of a data scientist in an organization?</h5>
            <p>Ans: Data scientists are responsible for collecting, analyzing, and interpreting complex datasets. They develop models, algorithms, and statistical analyses to extract valuable insights and inform decision-making.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">9:How does data science contribute to artificial intelligence (AI)?</h5>
            <p>Ans: Data science provides the foundation for AI by utilizing data to train and improve machine learning models, which are a key component of many AI applications.</p>
            <h5 class="text-gradient text-primary mb-0 mt-2">10:What are the ethical considerations in data science?</h5>
            <p>Ans: Ethical considerations in data science include issues related to privacy, bias in algorithms, transparency, and the responsible use of data, ensuring that the impact of data science activities is fair and equitable.</p>
